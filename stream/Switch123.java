package in.Java8Features.stream;

public class Switch123 {

	
	public static void main(String[] args) {
		
		switch(3) 
		
		{
		case 1: 
		
		System.out.println("My name is Raju");
		break;
		case 2: 
			System.out.println("My name is Krishna");
		break;	
		case 3: 
			System.out.println("My name is Anitha");
		break;
	}
}}